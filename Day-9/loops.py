#For loop
fruits = ['apple','Orange', 'banana']
for fruit in fruits:
    print(fruit)

#While loop
count =0
while count < 5:
    print(count)
    count+=1

#break Statement for loop

numbers = [1, 2, 3, 4, 5]
for number in numbers:
    if number ==3:
        break
    print(number)

#break statement while loop
count = 0
while count <=10:
    if count == 7:
        break
    count+=1
    print(count)